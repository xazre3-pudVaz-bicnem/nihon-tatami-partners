export function getProviderById(id: string): Provider | undefined {
  return MOCK_PROVIDERS.find((p) => p.id === id);
}

export function getProvidersByCity(city: string): Provider[] {
  return MOCK_PROVIDERS.filter(
    (p) => p.serviceAreas.some((a) => a.includes(city)) && p.status === "active"
  );
}

export function getTopProviders(limit = 6): Provider[] {
  return MOCK_PROVIDERS.filter((p) => p.status === "active")
    .sort((a, b) => (a.rank ?? 99) - (b.rank ?? 99))
    .slice(0, limit);
}

export function getHighRatedProviders(limit = 6): Provider[] {
  return MOCK_PROVIDERS.filter((p) => p.status === "active")
    .sort((a, b) => b.averageRating - a.averageRating)
    .slice(0, limit);
}

export function getNewProviders(limit = 4): Provider[] {
  return MOCK_PROVIDERS.filter((p) => p.status === "active")
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, limit);
}

// 同一エリアの関連業者（自分を除く）
export function getRelatedProviders(provider: Provider, limit = 3): Provider[] {
  return MOCK_PROVIDERS.filter(
    (p) =>
      p.id !== provider.id &&
      p.status === "active" &&
      (p.city === provider.city ||
        p.serviceAreas.some((a) => provider.serviceAreas.includes(a)))
  )
    .sort((a, b) => b.averageRating - a.averageRating)
    .slice(0, limit);
}
