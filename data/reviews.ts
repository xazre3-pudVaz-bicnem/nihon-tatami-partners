export function getReviewsByProviderId(providerId: string): Review[] {
  return MOCK_REVIEWS.filter((r) => r.providerId === providerId && r.status === "approved");
}

export function getRecentReviews(limit = 6): Review[] {
  return MOCK_REVIEWS.filter((r) => r.status === "approved")
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, limit);
}

export function calcAverageRating(reviews: Review[]): number {
  if (!reviews.length) return 0;
  const sum = reviews.reduce((acc, r) => acc + r.rating, 0);
  return Math.round((sum / reviews.length) * 10) / 10;
}
